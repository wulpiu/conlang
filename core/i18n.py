def set_lang(lang: str) -> None:
    global _lang
    if lang in _T:
        _lang = lang

def get_lang() -> str:
    return _lang

def tr(key: str, *args) -> str:
    text = _T.get(_lang, _T["ru"]).get(key, key)
    return text.format(*args) if args else text
